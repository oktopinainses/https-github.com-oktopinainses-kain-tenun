			      <h2><strong><center/>130 Ribu Warga Lombok Timur Belum Lakukan Perekaman E-KTP</strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="E-KTP-696x364.jpg" width="418" height="280" align="center"></p> 
			      <p>SSELONG – Dinas Kependudukan dan Pencatatan Sipil (Dukcapil) Kabupaten LombokTimur (Lotim), terus berupaya mengejar target perekaman dan pencetakan KTP Elektronik (E-KTP). Sejauh ini diperkirakan masih 130 ribu warga Lotim yang belum melakukan perekeman E-KTP.
					Sekdis Dukcapil Lotim, Azis, S.Pd, mengatakan bahwa untuk mengejar target itu, proses perekaman data tidak hanya dilakukan di tingkat kabupaten saja, melainkan perekaman juga dilakukan langsung hingga ke tingkat dusun. Sehingga nantinya pencapaian perekaman E-KTP bisa sesuai target yang telah ditentukan, sekitar 92 persen.<p/>
					“Kita sudah memanggil Pemerintah Kecamatan, dan Camat selanjutnya memanggil Kepala Desa, dan kemudian Kepala Desa giliran memanggil Kepala Dusun, yang kemudian diumuman melalui masjid, agar masyarakat bisa melakukan perekaman,” katanya Senin kemarin (18/12).
					Namun meski telah diumumkan melalui masjid, termasuk tak henti-henti memberikan imbauan di masing-masing desa. Namun hingga kini kesadaran masyarakat akan pentingnya E-KTP masih saja tergolong minim. ”Saya tidak tau apa peyebab sebenarnya. Yang jelas tim dari dinas sudah turun untuk melakukan perekaman. Tapi masih saja ada masyarakat yang tidak hadir, dengan alasan yang tidak jelas,” keluhnya.<p/>
					Saat ini sebut Sekdis Dukcapil, pencapaiakan E-KTP di Lotim sudah mencapai sekitar 85,69 persen, dengan sisa yang belum melakukan perekaman sebanyak 132.076 wajib E-KTP. Dengan banyaknya masyarakat yang belum melakukan perekeman, tentu pihaknya akan selalu berusaha mengejar, dan berharap pada Pilkada mendatang, target pencapaian telah terealiasi.<p/>
					Menurutnya, dari 20 Kecamatan di Lotim, Kecamatan Masbagik menjadi kecamatan yang warganya masih banyak belum melakukan perekaman E-KTP, dengan jumlah sebanyak 20.826 wajib E-KTP. Kemudian disusul oleh Kecamatan Jerowaru dengan jumlah sebanyak 16.098 orang, dan selanjutnya Kecamatan Terara dengan jumlah sebanyak 16.035 orang.<p/>
					Sementara Kecamatan Sakra Barat menempati urutan keempat kecamatan yang warganya masih banyak belum melakukan perekaman E-KTP, dengan jumlah sebanyak 14.911 orang. Disusul Kecamatan Pringgabaya dengan jumlah 14.631 orang, dan Kecamatan Suela dengan jumlah sebanyak 13.502 orang, dan Kecamatan Aik Mel sebanyak 13.327 orang.<p/>
					Selain itu, Kecamatan Labuhan Haji ada sebanyak 12.292 orang, Kecamatan Pringgasela sebanyak 11.774 orang, Kecamatan Wanasaba sebanyak 11.387 orang, dan Kecamatan Sakra Timur sebanyak 11.524 orang. ”Sementara kecamatan yang belum disebutkan, merupakan kecamatan yang jumlahnya dibawah 8 ribu. Tapi kita berharap, semoga wajib E-KTP ini bisa didorong agar segera melakukan perekaman,” harap Azis. (cr-wan)

