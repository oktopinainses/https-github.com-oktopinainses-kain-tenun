			      <h2><strong><center/>RSUD dr Soedjono Selong Lulus Akreditasi</strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="PELAYANAN-696x364.jpg" width="418" height="280" align="center"></p> 
			      <p>SELONG—Rumah Sakit Umum Daerah (RSUD) dr Raden Soedjono Selong, telah mengikuti proses akreditasi beberapa waktu lalu. Melalui website resminya, Komite Akreditas Rumah Sakit (KARS) merilis hasil akreditasi yang telah dilakukan di sejumlah RSUD seluruh Indonesia. Dimana salah satunya RSUD dr Soedjono  Selong dinyatakan lulus dengan poin tiga bintang.
					“Itu hanya kita lihat melalui webnya. Kalau secara resmi kita belum terima pengumumannya,” ungkap Direktur RSUD dr Seodjono Selong, Karsito, Rabu kemarin (19/12).<p/>
					Dikatakan, akreditasi yang telah dilakukan itu, dengan tujuan untuk menentukan standarisasi pelayanan. Jadi ketika lulus akreditasi, bukan berarti status dari rumah sakit ini juga harus disesuaikan.<p/>
					“Jadi akreditasi tidak ada kaitannya dengan kenaikan status. Tapi hanya berkaitan dengan standariasi pelayanan. Jadi semua pelayanan di rumah sakit itu harus punya standar. Sehingga tidak ada kaitannya, apakah rumah sakit itu tipe C, B, atau A,” lanjut dia.
					Dalam akreditasi itu, berbagai aspek dan kelengkapan yang dimiliki rumah sakit, semuanya tak luput dari penilaian. Baik itu tenaga medis, pelayanan, kelengkapan, adiministrasi, dan banyak hal lainnya. “Semua dinilai, termasuk kendaraan, peralatan medis dan juga fasilitas air,” lanjut dia.<p/>
					Dari akreditasi ini lanjutnya, maka proses pelayanan yang disiapkan rumah sakit kedepannya harus lebih baik lagi. Akreditiasi ini secara administratif juga menjadi salah satu syarat untuk bisa bekerjasama dengan Badan Pelayanan Jaminan Kesehatan (BPJS).
					Karena itu sampai 2019 mendatang, semua rumah sakit harus sudah menyandang status akreditasi. Jika tidak lulus, apalagi sampai tidak pernah di akreditasi, maka rumah sakit itu tidak akan bisa bekerjasama dengan BPJS.
					“Ini sudah menjadi syarat mutlak untuk kerjasama dengan BPJS. Dan ini akan mulai diberlakukan pada 2019 mendatang. Terlepas rumah sakit itu tipe C atau A, yang penting harus terakreditasi,” terang Karsito.<p/>
					Pelaksanaan akreditasi ini kata dia, dilakukan tiga tahun sekali. Sementara bagi rumah sakit yang sudah melakukan akreditasi, maka setiap tahunnya juga akan tetap dilakukan evaluasi. “Kalau kita lihat pengumuman di web KARS, kita dapatkan tiga bintang. Sementara yang terbaik lima bintang,” sebut dia.
					Terkait dengan hasil akreditasi ini, pihaknya akan menunggu pegumuman secara resmi. Hanya saja mereka sendiri juga belum mengetahui secara pasti, kapan pengumuman hasil akreditasi ini akan disampaikan. “Kita belum bisa pastikan. Yang jelas kalau diumumkan, pasti kita diundang,” tutup Karsito. (lie)
					</p>
