			      <h2><strong><center/>Eks Bangunan RSUD Lombok Utara Dirobohkan</strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="FA-ROBOH-696x364.jpg" width="418" height="280" align="center"></p> 
			      <p>TANJUNG – Eks bangunan Rumah Sakit Umum Daerah (RSUD) Lombok Utara akhirnya dirobohkan. Aset bangunan senilai ratusan juta ini sengaja dirobohkan untuk keperluan rencana pembangunan landscape yang telah disiapkan anggarannya di APBD tahun anggaran 2018. Hal ini dilakukan dalam upaya penataan pusat layanan kesehatan demi kenyaman dan keamanan para pasien dan pengunjung pasien.<P/>
					Kabag Tata Usaha RSUD Lombok Utara Zulfahrudin menerangkan, perobohan bangunan lama RSUD dalam rangka memanfaatkan lokasi untuk pembangunan landcape tahun anggaran 2018. Master plan pengerjaan landscape sudah ada. “Ini sengaja dirobohkan untuk pembangunan landscape yang disiapkan anggarannya. Mengenai teknis seluruhnya semua sudah ada di dalam master plan,” ungkapnya, Selasa (12/12).<P/>
					ahap awal yang sedang dilakukan adalah bangunan lama di depan RSUD sudah diratakan. Hal itu guna mempermudah implementasi landscape tersebut. Mengenai sejumlah barang di aset bangunan lama itu sudah dihibahkan ke beberapa pondok pesantren yang membutuhkan bahan-bahan bangunan. “Hibah ini sudah ada persetujuan daerah yang ditandatangi pak bupati,” jelasnya.<P/>
					Dengan masa transisi pembongkaran gedung lama, yang kemudian akan dibangun landscape pihaknya menilai tidak akan menggangu jalannya operasional pelayanan kesehatan kepada pasien. Pasalnya, beberapa ruangan seperti kamar jenazah dan, musalla telah dipindahkan terlebih dahulu di gedung belakang RSUD Tanjung. “Ada dua rungan sudah dipindah ke belakang. Termasuk kamar untuk pasien kemarin,” katanya.
					Dalam pengajuan pembangunan landscape sudah dilakukan pada pembahasan RAPBD 2018. Nilai yang diusulkan sebesar Rp 2,8 miliar, diharapkan pada proses pembanguna  berjalan lancar, sehingga kenyaman para pasien lebih lega. Di lokasi ini juga akan dibangunkan PAUD untuk penitipan anak pasien nantinya. “Kita usulkan sebesar Rp 2,8 miliar untuk tahun depan,” tandasnya.<P/>
					Sementara itu, langkah yang dilakukan RSUD Tanjung didukung oleh Ketua Komisi III DPRD Lombok Utara Nasrudin. Politisi Gerindra ini menilai, setuju kaitannya dalam meningkatkan pelayanan dari segi infrastruktur rumah sakit. “Kami setuju kalau untuk pelayanan, tetapi harus dilihat pembongkaran aset lama (lokasi 
					yang akan dibangun landscape) sudah sesuai atau tidak,” ucapnya singkat. (flo)

