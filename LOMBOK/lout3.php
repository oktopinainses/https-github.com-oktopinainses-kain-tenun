			      <h2><strong><center/>PT BAL Kembali Beroperasi, Gili Meno Masih Krisis Air Bersih</strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="gili-meno-lombok-696x364.jpg" width="418" height="280" align="center"></p> 
			      <p>TANJUNG – Distribusi air bersih di Gili Trawangan kembali normal. PT Berkait Air Laut (BAL) kembali beroperasi setelah sempat menghentikan operasional beberapa hari paska gedung produksinya disegel kepolisian. Pemerintah Kabupaten (Pemkab) Lombok Utara turun tangan dan menggelar pertemuan dengan PT BAL. Perusahaan ini bersedia kembali beroperasi melayani distribusi air bersih kepada warga dan pelaku wisata setempat. Namun pertemuan  ini tidak berpengaruh terhadap proses hukum yang dialami Direktur PT BAL Jhon Mathesjon. “Sudah beroperasi kemarin malam sekitar pukul 21.00 Wita, Jumat lalu (8/12). Sekarang sudah mengalir semuanya ke seluruh pelanggannya,” terang Camat Pemenang Faisal kepada Radar Lombok, Minggu  kemarin (10/12).<p/>
					Namun Faisal tidak bisa menjamin jika PT BAL masih tetap beroperasi. Pihaknya belum mengetahui keputusan perusahaan ini selanjutnya. “Kalau sekarang ini (jangka pendek), Insya Allah aman. Kalau kedepan belum tahu.Kita serahkan ke pemda kalau jangka panjangnya,” tandasnya.
					Sementara itu, Kades Gili Indah H  Taufik mengatakan, pendistribusian air bersih di Gili Trawangan sudah normal. Yang belum itu masyarakat pelanggan di Gili Meno sampai saat ini belum. “Di sana itu kemungkinan besarnya tidak bisa. Kalau di Trawangan dijamin lama belum berani memberikan kepastian, yang jelas sekarang ini bisa mengalir,” ungkapnya.<p/>
					Khusus Gili Meno, pihaknya sudah berkoordinasi dengan Pemkab Lombok Utara agar berusaha mendistribusikan air bersih dengan menggunakan fast boat. Kemungkinan hari ini pihaknya bersama Badan Penanggulangan Bencana Daerah (BPBD) dan Perusahaan Daerah Air Minum (PDAM) Lombok Utara sudah berkoordinasi untuk meninjau ke Gili Meno. “Kita akan memastikan mendistribusikan air ke sana memakai tadon-tadon yang ada nantinya. Tapi, hari ini akan mencoba memastikan di mana titik-titiknya,”<p/>
					Pendistribusian air bersih dari Pemkab Lombok Utara digunakan sebatas untuk keperluan air minum dan memasak sehar-hari. Sedangkan, air bersih ke hotel-hotel dan mandi belum bisa, karena harus membutuhkan air yang sangat besar. “Mungkin jangka pendek akan seperti itu dulu. Jangka panjangnya akan diusahakan nantinya,” tandasnya.<p/>
					Untuk kebutuhan air bersih di tiga gili kata Taufik, diserahkan sepenuhnya kepada Pemkab Lombok Utara. Entah langsung dikerjakan PDAM atau pihak investor. Yang terpenting kebutuhan air bersih di kawasan wisata ini segera terpenuhi. Sebab, jika dibiarkan berlarut-larut maka akan dikhawatirkan berpengaruh terhadap kenyaman para pelaku wisata dan wisatawan. “Siapapun itu kami harapkan segera mungkin bisa masuk. Kalau menggunakan pihak ketiga, kita mempersilakan tentunya harus mengantongi izin yang lengkap supaya tidak terjadi yang tidak diinginkan pada kemudian hari,” harapnya.<p/>
					Sementara itu, Kepala BPBD Lombok Utara Iwan Maret Asmara mengatakan, penyaluran air bersih ke Gili Meno pada  Minggu kemarin belum bisa dilakukan karena pihaknya harus berkoordinasi dengan instansi terkait. Pihaknya saat ini masih melakukan sebatas koordinasi untuk penyaluran air bersih. “Kita masih koordinasikan, hari ini belum bisa kita distribusikan,” terangnya.<p/>
					Menurutnya, kejadian ini spontan sehingga belum ada persiapan untuk menyalurkan air bersih.  “Itu yang kita masih kita diskusikan. Kita akan tunggu perintah nantinya. Insya Allah besok pagi (hari ini) kita akan rapat koordinasi semuanya,” tandasnya.
					Atas kondisi seperti ini, Pemkab Lombok Utara mendapatkan dorongan tegas dari  DPRD Lombok Utara untuk mengambil kebijakan jangka panjang. Sebab, kekurangan air bersih di dua lokasi  itu harus cepat ditangani. Salah satu tindakan yang dipercepat adalah pemerintah harus segera mempercepat proses Kerja sama Pemerintah dan Badan Usaha (PKBU) yang tengah digodok. “Pemerintah harus segera, jadi kita ada kesepakatan bagi siapapun yang mau yang masuk untuk diajak kerja sama,” imbunya Ketua Komisi II DPRD Lombok Utara Tusen Lasima terpisah.<p/>
					Sementara itu, Ketua Komisi I DPRD Lombok Utara Ardianto mengatakan, pada tahun 2015 PDAM pernah melakukan kajian dengan Universitas Mataram (Unram) terkait distribusi air bersih ke tiga gili dengan menggunakan pipa. Di mana, kala itu seluruh biaya yang timbul dibebankan pada APBD Lombok Utara. “Kajian Unram itu yang sudah dibiayai daerah itu. Silakan dimaksimalkan sekarang. Karena sampai sekarang kita belum tahu hasilnya seperti apa,” katanya.<p/>
					Solusi yang sudah jelas dengan menggandeng pihak ketiga, menurutnya harus dijalankan pemerintah dengan mekanisme yang sangat terbuka.  Sepanjang kerjasama dengan pemerintah ini tidak melakukan pengeboran maka dipersilakan. “Kalau melakukan pengeboran lantas apa bedanya dengan PT BAL dan kita harap pemerintah serius menangani hal ini,” harapnya. (flo)



