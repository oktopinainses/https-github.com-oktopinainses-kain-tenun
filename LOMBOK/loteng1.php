			      <h2><strong><center/>Ini Data Kerugian Akibat Puting Beliung di Beberapa Desa Lombok Tengah</strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="F-BENCANA-696x364.jpg" width="418" height="280" align="center"></p> 
			      <p>PRAYA – Badan Penanggulangan Bencana Daerah (BPBD) Lombok Tengah sudah mendata kerugian akibat bencana angin puting beliung yang terjadi di empat desa. Yakni, Desa Aikmual, Mertak Tombok Kecamatan Praya, Desa Muncan Kecamatan Kopang, dan Desa Kelebuh Kecamatan Praya Tengah.
					Hasilnya, BPBD menemukan ada 29 unit rumah warga yang rusak berat, sedang, dan tingan. Rincinya, untuk Desa Mertak Tombok 9 unit rusak berat, Aikmual 2 unit, Muncan 13  unit, dan Kelebuh 5 unit. Hal itu juga membantah data sebelumnya yang menyatakan bahwa di setiap desa yang tertimpa bencana tersebut terdapat puluhan rumah yang mengalami kerusakan. “Biasa kalau data awal masyarakat yang rumahnya hanya hilang genteng satu maka itu yang dilaporkan juga. Untuk kerusakan ringan memang ada, tapi berdasarkan hasil di lapangan untuk kerusakan yang parah setidaknya ada 29 rumah,” ungkap Kepala BPBD Lombok Tengah, H Muhammad kepada Radar Lombok, Sabtu lalu (16/12).
					Ditambahkan Muhammad, tidak ada korban jiwa dalam bencana ini. Namun, kerugian materil ditaksir mencapai ratusan juta. “Karena bangunan rumah milik warga yang bahkan ada rusak karena ditimpa pepohonan, maka kita taksir kerugian mencapai ratusan juta, dan kita masih terus melakukan pendataan,” tambahnya.<p/>
					Muhammad menjelaskan, pihaknya tidak membuatkan warga untuk tempat mengungsi karena warga juga lebih memilih untuk kembali ke rumah masing-masing. Untuk memperbaiki rumah dari puing reruntuhan yang masih tersisa. “Warga juga tidak ada yang mengungsi karena langsung balik kerumah masing-masing paska kejadian,” katanya.</p>p>
					Disampaikan juga, kebanyakan kerusakan yang terjadi di rumah warga terdapat pada atap dan tembok bangunan. Hal itu karena angin yang cukup kencang yang membuat atap-atap rumah warga yang kebanyakan terbuat dari genteng dan asbes itu menjadi rusak. “Tapi kita sudah melakukan pendataan dan kita salurkan para warga asbes untuk memperbaiki rumahnya,” lanjutnya.</p>
					Untuk saat ini, menurut Muhammad kondisi warga pascakejadian sudah mulai membaik. Warga masyarakat sudah mulai beraktivitas seperti biasanya dan memperbaiki kerusakan yang terjadi. “Masyarakat juga secara sukarela bergotong royong untuk membersihkan rumah warga yang rusak. Sehingga saat ini aktivitas warga masih normal,” tambahnya.<p/>
					Lebih jauh diterangkan Muhammad, bencana angin puting beliung itu terjadi sekitar pukul 15.45 Wita, Kamis lalu (14/12). Kejadian bermula ketika turun hujan lebat disertai angin kencang mengakibatkan puluhan rumah warga rusak. “Bahkan kalau di Mertak Tombok ada satu bangunan madrasah yang atapnya rusak ringan akibat angin dan beberapa pohon juga bertumbangan,” pungkasnya. (cr-met)


