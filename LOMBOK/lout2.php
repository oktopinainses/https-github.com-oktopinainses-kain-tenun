			      <h2><strong><center/>Lombok Utara Dapat Bantuan Kapal Berlayar</strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="kapal-bantuan-696x364.jpg" width="418" height="280" align="center"></p> 
			      <p>TANJUNG – Pemerintah Kabupaten Lombok Utara (KLU) mendapatkan bantuan satu unit kapal berlayar merek Cummins LTAA 2200 RPM-2X340 HP dari Kementerian Desa, Pembangunan Daerah Tertinggal dan Transmigrasi.
					Bantuan ini akan dijadikan kapal operasional ke tiga gili. “Kapal yang kita terima ini sesuai usulan  beberapa waktu lalu yang  dilakukan Dinas Perhubungan,” terang Sekretaris Dinas Perhubungan Kelautan dan Perikanan (Dishublutkan) Lombok Utara H Samsul Rizal kepada Radar Lombok Jumat kemarin (8/12).<p/>
					Sesuai arahan kementerian, katanya kapal ini  boleh dikelola BUMDes atau asosiasi atau BUMD. Tapi  sesuai usulan di proposal untuk asosiasi pengelola wisata teluk Nare. “Saat (menjabat) plt (kepala dinas) dulu saya mengusulkan. Kalau dimana arahkan nanti tergantung Pak Kadis. Cuma tidak boleh dikelola dinas, itu arahan dari dirjen,” ungkapnya.<p/>
					Penyerahan kapal ini diterima Sekda Lombok Utara H Suardi  Kamis lalu (7/12). Kapal ini akan langsung diterima di Pelabuhan Teluk Nare dengan menghabiskan perjalanan laut selama lima hari baru akan tiba. “Nanti kita akan terima di Teluk Nare satu unit,” jelasnya.
					Kapal ini diberikan nama  KM Tioq Tata Tunaq dengan ukuran panjang 19,00 meter, lebar 4,20 meter, dan dalam 2.10 meter. Sementara tonase kotor (GT) 46, tonase bersih (NT) 14, tahun pembuatan 2017, bahan utama Fiberglass, penggerakan mesin, jumlah bali dua. “Bantuan diharapkan sesuai ketentuan peraturan perundang-undangan,” pungkasnya.(flo)


