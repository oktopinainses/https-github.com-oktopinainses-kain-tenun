			      <h2><strong><center/>Muaythai Usulkan 4 Atlet Masuk Pelatda </strong></h2>
			      <p>&nbsp;</p>
			      <p><img src="MUAYTHAI-218x150.jpg" width="418" height="280" align="center"></p> 
			      <p>MATARAM—Pengurus Muaythai Indonesia (MI) NTB merencanakan akan mengusulkan 4 atletnya masuk skuad Pemusatan Latihan Daerah (Pelatda) NTB 2018 ini. Keempat atlet tersebut tentunya sudah mempunyai catatan prestasi yang bagus.
					Ketua Harian MI NTB Indera Gunawan mengatakan, pihaknya akan mengusulkan 4 atlet MI NTB. Usulan ini disebutnya diminta oleh Ketua Bidang Pembinaan Prestasi (Bimpres) Komite Olahraga Nasional Indoensia (KONI) NTB.
					“Kita disuruh mengusulkan atlet masuk skuad Pelatda NTB oleh Bimpres KONI dan itu langsung disampaikan saat kami rapat evaluasi dengan semua pelatih Pelatda yang dilaksanakan di sekretariat KONI NTB,’’ katanya kepada Radar Lombok, Selasa (2/1).
					Keempat atlet yang diusulkan ini bukan tanpa sebab. Mereka sudah mampu memberikan yang terbaik untuk NTB disetiap event yang diikuti. Keempat atlet tersebut adalah Adit Agustia, Ariya Yuzardi, Mujahidin dan Restu Muallimin.<p/>
					Dikehaui, saat ini hanya satu atlet MI yang masuk Pelatda NTB, yakni Ardiansyah. Andai keempat atlet ini masuk, otomatis ada 5 atlet muaythai di Pelatda NTB.
					Diakui, saat ini MI NTB memiliki kekurangan fasilitas latihan. Kekurangan tersebut meliputi ring dan lain-lainnya. Namun dengan kekurangan tersebut tidak berarti para atlet tidak latihan.
					Terpisah, Ketua Bidang Pembinaan Prestasi (Bimpres) Komite Olahraga Nasional Indoensia (KONI) NTB, Wibowo Budi Santoso menyatakan, atlet yang masuk Pelatda NTB saat ini ada 39 atlet. Dari 39 atlet yang ada, di tahun ini akan diupayakan bertambah. Mereka ini nantinya akan membela NTB di PON 2020. (cr-adi)
					</p>
